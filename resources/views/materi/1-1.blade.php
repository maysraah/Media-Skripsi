    <h5 style="color:#011B78">Tujuan Pembelajaran</h5>
    <p class="text-justify">
        Memperkenalkan konsep dasar dari pendekatan belajar <i>computational thinking.</i></p>
</div>

<div>
    <p>
        <img style="width: 310px;" src="{{ asset('storage/img/marker2.png') }}">
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        <b><i>Computational Thinking</i> (CT) atau Berpikir Komputasional</b> adalah pendekatan sistematis dalam berpikir kritis dan pemecahan masalah dengan <b>merumuskan persoalan</b> serta menyusun strategi yang <b>efektif, efisien, dan optimal.</b>
        CT menggunakan prinsip dan praktik ilmu komputer untuk menghasilkan solusi yang dapat dieksekusi oleh manusia maupun komputer. Pendekatan ini tidak hanya terbatas pada pemrograman, tetapi juga dapat diterapkan dalam berbagai bidang, terutama dalam dunia pendidikan, untuk membantu siswa mengembangkan keterampilan berpikir logis dan analitis.
    </p><br>

    <p>
        <img style="width: 320px;" src="{{ asset('storage/img/marker3.png') }}">
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        Sering kali terjadi kesalahan dalam membedakan permasalahan dan persoalan. Permasalahan adalah gejala yang terlihat, sedangkan persoalan merupakan akar penyebab dari gejala tersebut. Sebagai contoh, sakit kepala adalah gejala, tetapi penyebabnya bisa beragam, seperti kurang tidur, sakit gigi, atau kondisi medis lainnya.
        Penyebab inilah yang disebut sebagai persoalan. Untuk menyelesaikan persoalan, diperlukan solusi yang efektif, efisien, dan optimal:
    </p>
    <ul>
        <li class="text-justify"><b>Efektif</b> berarti solusi mampu mengatasi persoalan yang dihadapi.</li>
        <li class="text-justify"><b>Efisien</b> berkaitan dengan strategi yang digunakan serta pemanfaatan sumber daya yang tersedia.</li>
        <li class="text-justify"><b>Optimal</b> mempertimbangkan batasan tertentu. Misalnya, dalam memilih makanan, faktor seperti rasa, kesehatan, porsi, dan harga akan menentukan pilihan terbaik berdasarkan kebutuhan individu.</li>
    </ul><br>
    
    <p>
        <img style="width: 320px;" src="{{ asset('storage/img/marker4.png') }}">
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        CT memiliki empat fondasi yang menjadi landasan pemecahan persoalan yaitu <b>dekomposisi <i>(decomposition)</i>, pengenalan pola <i>(pattern recognition)</i>, abstraksi <i>(abstraction)</i> dan algoritma <i>(algorithm)</i>.</b>
        Mari kita pelajari empat fondasi tersebut di bagian selanjutnya!
    </p>
</div>