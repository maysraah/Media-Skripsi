<div>
    <p>
        <img style="width: 280px;" src="{{ asset('storage/img/marker6.png') }}">
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        <b>“Mencari”</b> adalah proses <b>menemukan sesuatu</b>, baik itu benda, angka, konsep, atau informasi, yang memenuhi kriteria tertentu dalam sebuah ruang pencarian.
        Masalah pencarian sangat umum terjadi dalam kehidupan sehari-hari, termasuk dalam dunia komputasi. Ketika kita melakukan pencarian,
        kita berusaha menemukan suatu objek yang memenuhi kriteria tertentu dari sekumpulan objek lainnya. Beberapa contoh masalah pencarian yang sering ditemui meliputi:
    </p>
    <ol>
        <li>Mencari buku dengan judul tertentu di rak buku perpustakaan.</li>
        <li>Mencari pakaian batik seragam di lemari yang berisi semua pakaian yang dimiliki.</li>
        <li>Mencari dokumen atau halaman web tertentu menggunakan mesin pencari seperti Google.</li>
    </ol>
    <p class="text-justify" style="text-indent: 2em;">
        Mencari benda fisik mungkin terlihat mudah, karena kita bisa melihat dan mencocokkannya dengan mata. Namun, mencari informasi atau konsep yang abstrak? 
        Hmm… itu tidak semudah yang dibayangkan!
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        <b>Algoritma pencarian <i>(searching)</i></b> adalah <b>serangkaian langkah</b> atau <b>instruksi</b> yang digunakan untuk <b>mencari</b> elemen atau informasi tertentu (yang biasa disebut dengan <b>kunci pencarian</b>) dalam sekumpulan data.
        Setelah proses pencarian dilaksanakan, akan diperoleh salah satu dari dua kemungkinan, yaitu data yang dicari ditemukan <i>(successful)</i> atau tidak ditemukan <i>(unsuccessful).</i>
        Dalam pemrograman, algoritma pencarian merupakan salah satu teknik penting untuk menyelesaikan berbagai masalah.
    </p>

    <p>
        <img style="width: 180px;" src="{{ asset('storage/img/marker7.png') }}">
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        Pencarian berfungsi untuk <b>mencocokkan</b> data atau <b>mencari</b> informasi yang dibutuhkan. Ada kalanya pencarian dilakukan dengan tujuan <b>hanya untuk mengetahui apakah data tersebut ada dalam sekumpulan data atau tidak</b>, 
        atau mungkin di lain waktu <b>posisi dari data yang dicari tersebut dibutuhkan untuk keperluan tertentu</b>, atau jika <b>kemunculan data lebih dari satu kali maka semua posisi dan frekuensi kemunculannnya ingin ditampilkan</b>.
    </p>
    <p class="text-justify" style="text-indent: 2em;">
        Proses pencarian data dilakukan dengan cara membandingkan informasi yang diinginkan dengan informasi yang tersedia.
        Saat melakukan pencarian, tugas kita adalah menemukan suatu objek yang sesuai dengan kriteria tertentu di antara banyak objek lainnya. Proses pencarian data ini juga dikenal sebagai <i>table look-up</i> atau <i>storage and retrieval information,</i>
        karena melibatkan pengumpulan sejumlah informasi dalam memori komputer dan kemudian mencari kembali informasi yang dibutuhkan dengan cepat. Hasil dari proses pencarian ini bisa berupa dua kemungkinan: data yang dicari ditemukan <i>(successful)</i> atau tidak ditemukan <i>(unsuccessful).</i>
    </p>
</div>


