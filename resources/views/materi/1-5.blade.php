<h5 style="color:#011B78">(4) Algoritma</h5>

<div style="display: flex; justify-content: center; align-items: center;">
    <img class="centered" style="width: 350px;" src="{{ asset('storage/img/bahan-kue.jpeg') }}" alt="Alat dan Bahan Kue">
</div><br>

<p class="text-justify" style="text-indent: 2em; padding-left: 20px;">
    Pernahkah kamu melakukan kegiatan memasak? Memasak resep makanan adalah aktivitas yang sangat mirip dengan mengikuti algoritma. Ketika kita memasak, kita sebenarnya sedang menerapkan algoritma yang terdiri dari serangkaian instruksi yang sistematis.
    Setiap resep memiliki urutan langkah yang harus diikuti dengan tepat, mulai dari menyiapkan bahan hingga memasak dengan suhu dan waktu tertentu.
</p>

<p class="text-justify" style="text-indent: 2em; padding-left: 20px;">
    Konsep ini sangat relevan ketika kita mempelajari algoritma dalam <i>computational thinking.</i> Sama seperti memasak, algoritma adalah urutan langkah yang logis dan sistematis untuk mencapai tujuan tertentu. Setiap langkah dalam algoritma harus dieksekusi secara berurutan dan tepat untuk menghasilkan hasil yang diinginkan.
    Dengan memahami bahwa algoritma adalah panduan langkah demi langkah yang menyerupai resep makanan, kalian dapat lebih mudah memahami pentingnya mengikuti prosedur yang terstruktur saat menyelesaikan masalah atau menulis program. Hal ini menunjukkan bahwa algoritma bukan sekadar teori, tetapi juga sesuatu yang sangat praktis dan memiliki keterkaitan langsung dengan aktivitas sehari-hari.
</p>